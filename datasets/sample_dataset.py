from torch.utils.data import Dataset
import os


class SampleDataset(Dataset):
    """
    Just an example sample dataset that can be used for debugging.
    Reads data generated by ChatGPT about New England :D
    """
    def __init__(self, split='split1', **kwargs):
        self.split = split
        assert split in ['split1', 'split2']
        with open(os.path.join('data', 'sample', f'{self.split}.txt')) as f:
            self.data = [line.strip() for line in f.readlines()]

    def __getitem__(self, idx):
        return {"content": self.data[idx]}

    def __len__(self):
        return len(self.data)
